function dbDateFormat($date='',$isOnlyDate = false){
    if($date != '' && $isOnlyDate==false){
        return date('Y-m-d H:i:s',strtotime($date));
    }
    if($date != '' && $isOnlyDate==true){
        return date('Y-m-d',strtotime($date));
    }
    return date('Y-m-d H:i:s');
}

